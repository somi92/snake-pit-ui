    private void jbtnRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRunActionPerformed
        String routine = jtxtRoutine.getText().trim();
        if(routine == null || routine.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cannot load empty routine.");
        } else {
            dispose();
            String res = core.game(routine, 50);
            System.out.println(res);
        }
    }//GEN-LAST:event_jbtnRunActionPerformed

