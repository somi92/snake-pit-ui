"   (if-food-right (move-forward) (move-forward))\n" +
"   (if-danger-two-ahead\n" +
"    (if-danger-left (turn-right) (turn-left))\n" +
"    (turn-left))))\n" +
" (if-danger-right\n" +
"  (if-moving-up\n" +
"   (if-moving-up\n" +
"    (if-food-up\n" +
"     (if-food-right (move-forward) (move-forward))\n" +
"     (if-danger-right (turn-left) (turn-right)))\n" +
"    (if-danger-ahead\n" +
"     (if-food-up (move-forward) (turn-right))\n" +
"     (if-moving-right (turn-right) (turn-right))))\n" +
"   (if-food-ahead (move-forward) (move-forward)))\n" +
"  (if-moving-down\n" +
"   (turn-left)\n" +
"   (if-food-right\n" +
"    (if-moving-right\n" +
"     (if-food-up (move-forward) (turn-left))\n" +
"     (if-moving-down (move-forward) (turn-right)))\n" +
"    (if-danger-ahead\n" +
"     (if-moving-right (move-forward) (turn-right))\n" +
"     (if-moving-right (turn-left) (move-forward)))))))";
        
        jtxtRoutine.setText(demo);
